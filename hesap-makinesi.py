a = int(input("Ilk sayiyi giriniz."+'\n'))
b = int(input("Ilk sayiyi giriniz."+'\n'))
c = input("Islem turunu seciniz."+'\n')
if c == "+":
    print(a+b)
if c == "-":
    print(a-b)
if c == "*":
    print(a*b)
if c == "/":
    print(a/b)
else:
    print("Oops! bir seyler yanlis gitti.")
